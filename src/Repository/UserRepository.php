    public function findAllResultat(){

         $conn = $this->getEntityManager()->getConnection();

        $sql = '
            SELECT count(r.id) as nbrealise , u.id as idEleve, u.nom as nom , u.prenom as prenom, u.moyenne_qcm as moyenne,u.nb_qcm_realises as nbQcmRea
            FROM user u
            INNER JOIN resultat r
            WHERE u.id=r.eleve_id 
            GROUP BY r.eleve_id
        ';
        $stmt = $conn->prepare($sql);
        $stmt->execute();

        // returns an array of arrays (i.e. a raw data set)
        return $stmt->fetchAll();
    }
